public class Eksponen {
	public static void main (String [] args) {

int x = 100;
System.out.println("Nilai dari e pangkat 100 \t= " + Math.exp(x));
System.out.println("Nilai dari ln 100 \t\t= " + Math.log(x));
System.out.println("Basis logaritma 10 dari 100 \t= " + Math.log10(x));
System.out.println("Nilai dari 2 pangkat 5 \t\t= " + Math.pow(2, 5));
System.out.println("Nilai dari akar 100 \t\t= " + Math.sqrt(x));
}
}